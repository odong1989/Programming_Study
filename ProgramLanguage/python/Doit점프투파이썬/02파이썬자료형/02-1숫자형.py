###4.숫자형을 활용하는 연산자들 
#4.1. +,-,*연산자
a=3
b=4
print(a+b)
print(a-b)
print(a*b)

#4.2. ** 연산자(제곱연산)
print(a,"**",b,":",a**b)

#4.3. % 연산자(나눗셈 후 나머지값 리턴)
