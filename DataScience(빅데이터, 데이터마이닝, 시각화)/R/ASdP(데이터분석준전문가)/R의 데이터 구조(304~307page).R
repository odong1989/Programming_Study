#R의 데이터 구조
#R의 데이터구조는 크게 아래처럼 나뉜다.
#[1]벡터
#[2]행렬
#[3]데이터 프레임


#[1] 벡터 형
#     벡터형 : 하나의 스칼라값, 혹은 2개 이상의 스칼라 원소들을 갖는 형태.
#                 숫자,문자, 논리연산자가 들어갈 수 있고 들어간 값에 따라 벡터의 성격도 달라진다.    

#숫자형 벡터
X= c(1,10,24,40) 
#c : connection(연결)을 의미함.
# = : c,자바 처럼 왼쪽으로 값을 넣는다는 의미.

#문자형 벡터
Y= c('사과','바나나','오렌지')
#책에서는 "사과"식으로 했는데 예상못한 에러라며 튕김;;

#논리연산자형 벡터
Z = c(TRUE, FALSE, TRUE);
#책 304 페이지 끝—————————

X 
Y 
Z

