#[2] 행렬(matrix)
#m*n형태의 직사각형에 데이터를 나열한 데이터 구조.
#똑같은 자료형들의 자료들만 모을 수 있다.
#옵션 1)  ncol : 열의 개수를 결정한다.
#옵션 2)  nrow :  행의 개수를 결정한다.

Mx= matrix(c(1,2,3,4,5,6), ncol=2)
Mx
#[출력결과]
#                [,1]    [,2]
#[1,]            1        4
#[2,]            2        5
#[3,]            3        6

#책 305페이지 끝—————————————————————-

#책 306페이지 시작—————————————————-
#벡터를 합쳐서 행렬로 만들기 
#rbind, cbind라는 명령어를 활용하여 만든다.

Mx
#[출력결과]
#                    [,1]    [,2]
#[1,]                1        4
#[2,]                2        5
#[3,]                3        6


R1=c(10,10)
R2=c(20,20,20)

#rbind : 기존의 행렬에 행을 추가하는 형태로 데이터를 결합시키는 명령어(rbind의 r은 row(행)를 뜻함)

rbind(Mx, R1)
#[출력결과]
#            [,1]    [,2]
#            1        4
#            2        5
#            3        6
#r1        10     10   

cbind(Mx, R2)
#[출력결과]
#            [,1]    [,2]   R2
#            1        4    20
#            2        5    20
#            3        6    20

#[3]데이터 프레임
#데이터 프레임 : 행렬과 유사한 2차원 목록 데이터 구조. 가장 널리쓰인다.
#                      행렬과는 다르게 각 열의 자료형이 달라도 구성이 가능하다(이것이 장점&널리쓰이는 이유)
# 장점 (1) 다루기 쉽다
#        (2) 한 번에 많은 정보를 담을 수 있다.
#        (3) 각 열이 서로 다른 데이터 타입을 가질 수 있다.(행렬경우 데이터유형이 다같아야 함)

#데이터 프레임 관련명령어 
#data.frame : 여러 개의 벡터를 하나의 데이터 프레임으로 합쳐 입력할 수 있다.

